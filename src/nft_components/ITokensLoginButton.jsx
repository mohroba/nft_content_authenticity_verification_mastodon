function ITokensLoginButton() {
    const [provider, setProvider] = useState(null);
    const [account, setAccount] = useState(null);
    const [contract, setContract] = useState(null);
    const [tokens, setTokens] = useState(null);
    const [iTokenBalance, setITokenBalance] = useState(0);

    const initializeWeb3 = async () => {
        try {
            if (typeof window.ethereum !== 'undefined') {
                const web3Provider = new ethers.providers.Web3Provider(window.ethereum);
                await window.ethereum.enable();

                setProvider(web3Provider);
            } else {
                // Handle the case when MetaMask is not available
                console.error('MetaMask not detected. Please install MetaMask.');
            }
        } catch (error) {
            console.error('Error initializing Web3:', error);
            // Handle errors appropriately
        }
    };

    const checkITokenBalance = async () => {
        if (provider && account) {
            try {
                const iTokenContract = new ethers.Contract('0xE707517587D9A4Ac063A133F8726dC48536c5E01', iTokensABI, provider);
                const balance = await iTokenContract.balanceOf(account);

                setITokenBalance(balance.toNumber());
                setContract(iTokenContract);
                const retTokens  = await getAllITokensOwnedByUser(account)
                setTokens(retTokens);
            } catch (error) {
                console.error('Error checking iTokens balance:', error);
                // Handle errors appropriately
            }
        }
    };

    const handleLogin = async () => {
        try {
            const accounts = await provider.listAccounts();
            if (accounts.length > 0) {
                setAccount(accounts[0]);
                checkITokenBalance();
            }
        } catch (error) {
            console.error('Error logging in:', error);
            // Handle errors appropriately
        }
    };
    async function getAllITokensOwnedByUser(userAddress) {
        const itokenContract = new ethers.Contract('0xE707517587D9A4Ac063A133F8726dC48536c5E01', iTokensABI, provider);
        const balance = await itokenContract.balanceOf(userAddress);
        let iTokens = [];
        for (let i = 0; i < balance; i++) {
            let tokenId = await itokenContract.tokenOfOwnerByIndex(userAddress, i);
            let token = await itokenContract.getNFT(tokenId);
            console.log(token);
            iTokens.push({
                id:tokenId.toString(),
                decayRate:token.decayRate.toString(),
                didDocumentUri:token.didDocumentUri.toString(),
                lastUpdated:token.lastUpdated.toString(),
                uri:token.uri.toString(),
                weight:token.weight.toString(),
            });
        }

        return iTokens;
    }

    useEffect(() => {
        initializeWeb3();
    }, []);

    return (
        <Fragment>
            <button onClick={handleLogin}>Login with IToken | ورود با توکن محتوا</button>
            {account && (
                <div>
                    <p>Connected as: {account}</p>
                    <p>IToken Balance: {iTokenBalance}</p>
                    <p>Tokens: {tokens?.length>0 && tokens.map(token=><button>{token.id}</button>)}</p>
                </div>
            )}

        </Fragment>
    );
}

export default ITokensLoginButton;
